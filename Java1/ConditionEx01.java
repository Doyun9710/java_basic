public class ConditionEx01 {
    public static void main(String[] args) {
        System.out.println( "시작" );

        int n = 10;
        if( n != 10) {
            System.out.println( "10 아님" );
        }
        else {
            System.out.println( "10 이다" );
        }

        System.out.println( "끝" );
    }
}
